def Add(addend1, addend2):
    return addend1 + addend2

sum = Add(5, 35)
print(sum)

def Subtract(number1, number2):
    return number1 - number2

difference = Subtract(40, 10)
print(difference)

def Multiply(num1, num2):
    return num1 * num2

product = Multiply(7, 10)
print(product)

def Divide(div1, div2):
    return div1 / div2

quotient = Divide(60, 10)
print(quotient)

